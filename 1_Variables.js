//* 1. *Variables* - Write a program that declares a variable using let, var, const, assigns a value to it, and logs it to the console. Then, try to reassign it and log the new value.

let x = 1;
var y = 2;
const z = 3;


console.log(x);       //? 1
console.log(y);       //? 2
console.log(z);       //? 3 



//* let variable
x = 11;
console.log(x);       //? 11     



//* var variable 
y = 21;
console.log(y);       //? 21




//* const variable 
z = 31;
console.log(z);       //? Error (because we can't re-assign the value to a const variable)